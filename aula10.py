# Além de somar dois números (int e float), o sinal de + pode também fazer concatenação.
# Não é possível usar o sinal de + com int e str, apenas int/float e str/str.
concatenacao = 'A' + 'B' + 'C' # Nesse caso estão sendo usados somente str, então o Python sabe.
print(concatenacao)

# O sinal de * pode fazer repetição // Entre int e str
a_dez_vezes = 'A' * 10
tres_vezes_fernanda = 3 * 'Fernanda'
print(a_dez_vezes)
print(tres_vezes_fernanda)